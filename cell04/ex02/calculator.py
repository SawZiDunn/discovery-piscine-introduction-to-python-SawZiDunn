#!/usr/bin/env python3

print("Give me the first number: ", end="")
first_number = int(input())


print("Give me the second number: ", end="")
second_number = int(input())

print("Thank you!")

print(f"{first_number} + {second_number} = {first_number + second_number}")
print(f"{first_number} - {second_number} = {first_number - second_number}")
print(f"{first_number} / {second_number} = {first_number / second_number}")
print(f"{first_number} * {second_number} = {first_number * second_number}")